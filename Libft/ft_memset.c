#include <stdio.h>

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	g;
	char			*t;

	t = s;
	i = 0;
	while (i < n)
	{
		g = c;
		t[i] = g;
		i++;
	}
	return (t);
}
